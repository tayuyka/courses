for name in student_names:
    student_groups = Group.objects.all()

    # Определение количества групп для студента (от 1 до 4)
    groups_count = random.randint(1, min(4, len(student_groups)))

    # Получение случайных групп
    selected_groups = random.sample(list(student_groups), groups_count)

    # Сортировка групп по количеству студентов и выбор наименее загруженных
    sorted_groups = sorted(selected_groups, key=lambda group: group.student_set.count())

    # Создание студента
    student = Student.objects.create(full_name=name)

    # Проверка на минимальное количество групп и максимальное количество групп для студента
    if 1 <= len(sorted_groups) <= 4:
        student.group.add(*sorted_groups)
    elif len(sorted_groups) == 0:
        # В случае, если у студента нет групп, добавляем его в случайную группу
        random_group = random.choice(list(student_groups))
        student.group.add(random_group)
    else:
        # Если студенту было выбрано больше 4 групп, добавляем его только в первые 4
        student.group.add(*sorted_groups[:4])


